package com.CoreJava_HW.pattern_printing;

public class Printing_7 {
    public static void main(String[] args) {
        for (int i = 1; i <=5 ; i++) {
            for (int j = 1; j <=5 ; j++) {
                System.out.print("# ");
            }
            System.out.println();
        }
    }
}
