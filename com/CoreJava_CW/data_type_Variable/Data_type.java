package com.CoreJava_CW.data_type_Variable;

public class Data_type {
    public static void main(String[] args) {
        System.out.println("Hello..");
        byte a = 1;
        short b = 2;
        int c = 3;
        long d = 4;
        float e = 44.4f;
        double f= 55.5;
        char ch= 'D';
        String str = "Dipanshu";
        boolean isGreater = a>b;

        System.out.println("It is byte value : "+a);
        System.out.println("It is short value : "+b);
        System.out.println("It is int value : "+c);
        System.out.println("It is long value : "+d);
        System.out.println("It is float value : "+e);
        System.out.println("It is double value : "+f);
        System.out.println("It is char value : "+ch);
        System.out.println("It is string value : "+str);
        System.out.println("It is boolean value : "+isGreater   );



    }
}
