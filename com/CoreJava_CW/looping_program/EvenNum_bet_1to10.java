package com.CoreJava_CW.looping_program;

public class EvenNum_bet_1to10 {
    public static void main(String[] args) {
        System.out.println("Even Number are -> ");
        for (int i = 1; i <=10 ; i++) {
            if (i%2==0)
            {
                System.out.println(i);
            }
        }

        System.out.println("Odd Number are -> ");
        for (int i = 1; i <=10 ; i++) {
            if (i%2==0)
            {
                System.out.println(i);
            }
        }
    }
}
